package com.example.democode.domain.sample.simple.stream.exam03;

public class Student {
    // Fields
    private String name;
    private int score;

    // Constructors
    public Student(String name, int score) {
        this.name = name;
        this.score = score;
    }

    // Methods
    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }
}
